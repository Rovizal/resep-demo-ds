function toastShow(message, type = "success") {
    const toastElement = document.querySelector(".toast-ex");
    const toastMessage = toastElement.querySelector(".toast-message");

    // Reset previous type classes
    toastElement.classList.remove(
        "toast-success",
        "toast-error",
        "toast-warning",
        "toast-info"
    );
    toastElement.classList.add(`toast-${type}`);

    // Set message text
    toastMessage.innerHTML = message;

    // Get or create instance
    const toastInstance = bootstrap.Toast.getOrCreateInstance(toastElement);

    // Delay for animation (if already visible)
    if (toastElement.classList.contains("show")) {
        toastInstance.hide();
        setTimeout(() => toastInstance.show(), 200);
    } else {
        toastInstance.show();
    }
}

function base64UrlSafeEncode(data) {
    // Check if data is an object (excluding null)
    if (typeof data === "object" && data !== null) {
        // Convert object to JSON
        data = JSON.stringify(data);
    }

    // First layer of encoding
    let enc1 = btoa(data);

    // URL-safe encoding: Replace characters to make it URL-safe
    const encFinal = enc1
        .replace(/\+/g, "-")
        .replace(/\//g, "_")
        .replace(/=+$/, "");

    return encFinal;
}

function base64UrlSafeDecode(data) {
    // Replace URL-safe characters to Base64 characters
    let replacedInput = data.replace(/-/g, "+").replace(/_/g, "/");

    // Add padding if necessary
    const padding = replacedInput.length % 4;
    if (padding) {
        replacedInput += "=".repeat(4 - padding);
    }

    // Fifth layer of decoding
    let dec5 = atob(replacedInput);

    // Parse JSON back to original data
    const originalData = JSON.parse(dec5);

    return originalData;
}

