// Global loader controller (POST-only)
(function () {
    // Track concurrent POST requests to avoid flicker
    let postInFlight = 0;
    const el = document.getElementById("loader");

    function showLoader() {
        // Remove Bootstrap's d-none
        el && el.classList.remove("d-none");
    }

    function hideLoader() {
        // Add d-none when no more POST requests
        if (postInFlight <= 0 && el) el.classList.add("d-none");
    }

    // jQuery global hooks
    $(document).ajaxSend(function (evt, jqXHR, settings) {
        const method = (settings.type || settings.method || "").toUpperCase();
        if (method === "POST") {
            postInFlight++;
            showLoader();
        }
    });

    // Fires for both success and error
    $(document).ajaxComplete(function (evt, jqXHR, settings) {
        const method = (settings.type || settings.method || "").toUpperCase();
        if (method === "POST") {
            postInFlight = Math.max(0, postInFlight - 1);
            hideLoader();
        }
    });

    // Extra safety: if a POST hard-fails early
    $(document).ajaxError(function (evt, jqXHR, settings) {
        const method = (settings.type || settings.method || "").toUpperCase();
        if (method === "POST") {
            postInFlight = Math.max(0, postInFlight - 1);
            hideLoader();
        }
    });
})();

